// Variables Basics in JavaScript

// Example 1: Store your name
let name = "Ace";
console.log("My name is " + name);

// Example 2: Store age
let age = 20;
console.log("I am " + age + " years old");

// Example 3: Reassign variable
let city = "Chennai";
console.log("I live in " + city);
city = "Bangalore";
console.log("Now I moved to " + city);

// Example 4: Use const (cannot be changed)
const country = "India";
console.log("I am from " + country);

// Example 5: Simple math
let a = 10;
let b = 5;
let sum = a + b;
console.log("The sum is: " + sum);
